class A2_P3_Pattern2{
	public static void main(String [] args){
		
		
		
	for(int i=1; i<=9; i++){
		
		//spaces
		for(int spaces=9; spaces>i; spaces--){
			System.out.print(" ");
		}
		
		
		
		
		for(int j=1; j<=i; j++){
			System.out.print(j + " ");
		}
		
		
		System.out.println();
	}
	}
}
	