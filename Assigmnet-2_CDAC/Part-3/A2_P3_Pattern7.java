class A2_P3_Pattern7{
	public static void main(String [] args){
		
		
		
	for(int i=9; i>=1; i--){
		
		
		
		//spaces
		for(int spaces=9; spaces>i; spaces--){
			System.out.print(" ");
		}
		
		
		
		
		for(int j=1; j<=i; j++){
			System.out.print(i +" ");
		}
		
		
		
		
		
		System.out.println();
	}
	}
}